#List
# Creating a list
my_list = [1, 2, 3, 4, 5]

# Modifying a list
my_list.append(6)
my_list[2] = 10

# Accessing elements
print(my_list[0])  # Output: 1
print(my_list)     # Output: [1, 2, 10, 4, 5, 6]

#Practical Example
# List of names
names = ['Kelash', 'Zeesan', 'Saleh', 'Danish']

# Adding a new name
names.append('Jai')

# Printing all names
for name in names:
    print(name)
